#include <stdio.h>
/**
 *  add - add two integers
 *  @a: first integer
 *  @b: second integer
 *  Return: add
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 *  sub - subtract two integers
 *  @a: first integer
 *  @b: second integer
 *  Return: minus last result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 *  div - divides two integers
 *  @a: first integer
 *  @b: second integer
 *  Return: division
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 *  mod - mod of two integers
 *  @a: first integer
 *  @b: second integer
 *  Return: last answer
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 *  mul - multiplies two integers
 *  @a: first integer
 *  @b: second integer
 *  Return: multiply
 */
int mul(int a, int b)
{
	return (a * b);
}
